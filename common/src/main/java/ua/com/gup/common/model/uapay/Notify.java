package ua.com.gup.common.model.uapay;

public class Notify {
    private String email;
    private String phone;

    public Notify() {
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
