package ua.com.gup.model.offer;


public class OfferStatistic {

    private int views;

    private int phoneViews;

    private int favorites;

    public int getViews() {
        return views;
    }

    public void setViews(int views) {
        this.views = views;
    }

    public int getPhoneViews() {
        return phoneViews;
    }

    public void setPhoneViews(int phoneViews) {
        this.phoneViews = phoneViews;
    }

    public int getFavorites() {
        return favorites;
    }

    public void setFavorites(int favorites) {
        this.favorites = favorites;
    }
}
